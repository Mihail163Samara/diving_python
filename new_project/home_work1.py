'''3. Напишите код, который запрашивает число и сообщает является ли оно простым или составным. Используйте правило для проверки: “Число является простым, если делится нацело только на единицу и на себя”. Сделайте ограничение на ввод отрицательных чисел и чисел больше 100 тысяч.'''

# Запрашиваем у пользователя ввод числа
num = int(input("Введите положительное число от 1 до 100000: "))

# Проверяем, находится ли число в пределах допустимых значений
if num < 1 or num > 100000:
    print("Ошибка: введите число в диапазоне от 1 до 100000.")
else:
    # Если число 1, оно ни простое, ни составное
    if num == 1:
        print("Число 1 не является ни простым, ни составным.")
    else:
        # Проверка числа на простоту
        is_prime = True  # Предполагаем, что число простое

        # Перебираем все числа от 2 до квадратного корня из num
        for i in range(2, int(num ** 0.5) + 1):
            if num % i == 0:
                is_prime = False  # Если число делится нацело, оно составное
                break

        # Выводим результат
        if is_prime:
            print(f"Число {num} является простым.")
        else:
            print(f"Число {num} является составным.")



